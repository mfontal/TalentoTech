// Declara una variable para almacenar la suma
let suma = 0;
// Declara una variable de control
let i = 1;
// Usa un bucle do...while para sumar los números del 1 al 50
do {
suma += i;
i++;
} while (i <= 50);
// Imprime el resultado en la consola
console.log("La suma de los números del 1 al 50 es:", suma);